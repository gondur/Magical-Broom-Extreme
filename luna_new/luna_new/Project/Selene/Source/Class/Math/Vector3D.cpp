//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CVector3D::CVector3D()
{
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CVector3D::CVector3D( const CVector3D &v )
{
	*this = v;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CVector3D::CVector3D( Float fPx, Float fPy, Float fPz )
{
	this->x = fPx;
	this->y = fPy;
	this->z = fPz;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
Float CVector3D::LengthSq( void )
{
	return ((x * x) + (y * y) + (z * z));
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
Float CVector3D::Length( void )
{
	return sqrtf( LengthSq() );
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
Float CVector3D::Dot( CVector3D &In )
{
	return ((x * In.x) + (y * In.y) + (z * In.z));
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Normalize( CVector3D &In )
{
	Float div = 1.0f / In.Length();
	x = In.x * div;
	y = In.y * div;
	z = In.z * div;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Cross( CVector3D &In1, CVector3D &In2 )
{
	x = (In1.y * In2.z) - (In1.z * In2.y);
	y = (In1.z * In2.x) - (In1.x * In2.z);
	z = (In1.x * In2.y) - (In1.y * In2.x);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Reflect( CVector3D &In1, CVector3D &In2 )
{
	Float dot = In1.Dot( In2 );

	x = In1.x - (2 * dot * In2.x);
	y = In1.y - (2 * dot * In2.y);
	z = In1.z - (2 * dot * In2.z);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformNormal( CVector3D &In1, CMatrix &In2 )
{
	x = (In1.x * In2.x.x) + (In1.y * In2.y.x) + (In1.z * In2.z.x);
	y = (In1.x * In2.x.y) + (In1.y * In2.y.y) + (In1.z * In2.z.y);
	z = (In1.x * In2.x.z) + (In1.y * In2.y.z) + (In1.z * In2.z.z);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformCoord( CVector3D &In1, CMatrix &In2 )
{
	x = (In1.x * In2.x.x) + (In1.y * In2.y.x) + (In1.z * In2.z.x) + In2.w.x;
	y = (In1.x * In2.x.y) + (In1.y * In2.y.y) + (In1.z * In2.z.y) + In2.w.y;
	z = (In1.x * In2.x.z) + (In1.y * In2.y.z) + (In1.z * In2.z.z) + In2.w.z;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformProjection( CVector3D &In1, CMatrix &In2 )
{
	Float _x = (In1.x * In2.x.x) + (In1.y * In2.y.x) + (In1.z * In2.z.x) + In2.w.x;
	Float _y = (In1.x * In2.x.y) + (In1.y * In2.y.y) + (In1.z * In2.z.y) + In2.w.y;
	Float _z = (In1.x * In2.x.z) + (In1.y * In2.y.z) + (In1.z * In2.z.z) + In2.w.z;
	Float _w = (In1.x * In2.x.w) + (In1.y * In2.y.w) + (In1.z * In2.z.w) + In2.w.w;

	_w = 1.0f / _w;

	x = _x * _w;
	y = _y * _w;
	z = _z * _w;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Normalize( void )
{
	Float div = 1.0f / Length();
	x *= div;
	y *= div;
	z *= div;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Cross( CVector3D &In )
{
	CVector3D vTemp = *this;
	x = (vTemp.y * In.z) - (vTemp.z * In.y);
	y = (vTemp.z * In.x) - (vTemp.x * In.z);
	z = (vTemp.x * In.y) - (vTemp.y * In.x);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Reflect( CVector3D &In )
{
	CVector3D vTemp = *this;
	Float dot = In.Dot( vTemp );

	x = In.x - (2 * dot * vTemp.x);
	y = In.y - (2 * dot * vTemp.y);
	z = In.z - (2 * dot * vTemp.z);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformNormal( CMatrix &In )
{
	CVector3D vTemp = *this;
	x = (vTemp.x * In.x.x) + (vTemp.y * In.y.x) + (vTemp.z * In.z.x);
	y = (vTemp.x * In.x.y) + (vTemp.y * In.y.y) + (vTemp.z * In.z.y);
	z = (vTemp.x * In.x.z) + (vTemp.y * In.y.z) + (vTemp.z * In.z.z);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformCoord( CMatrix &In )
{
	CVector3D vTemp = *this;
	x = (vTemp.x * In.x.x) + (vTemp.y * In.y.x) + (vTemp.z * In.z.x) + In.w.x;
	y = (vTemp.x * In.x.y) + (vTemp.y * In.y.y) + (vTemp.z * In.z.y) + In.w.y;
	z = (vTemp.x * In.x.z) + (vTemp.y * In.y.z) + (vTemp.z * In.z.z) + In.w.z;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::TransformProjection( CMatrix &In )
{
	CVector3D vTemp = *this;
	Float _x = (vTemp.x * In.x.x) + (vTemp.y * In.y.x) + (vTemp.z * In.z.x) + In.w.x;
	Float _y = (vTemp.x * In.x.y) + (vTemp.y * In.y.y) + (vTemp.z * In.z.y) + In.w.y;
	Float _z = (vTemp.x * In.x.z) + (vTemp.y * In.y.z) + (vTemp.z * In.z.z) + In.w.z;
	Float _w = (vTemp.x * In.x.w) + (vTemp.y * In.y.w) + (vTemp.z * In.z.w) + In.w.w;

	_w = 1.0f / _w;

	x = _x * _w;
	y = _y * _w;
	z = _z * _w;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::RotationX( Sint32 Angle )
{
	CVector3D vTemp = *this;
	Float s = Math::Sin( Angle );
	Float c = Math::Cos( Angle );

	x = vTemp.x;
	y = (vTemp.y * +c) + (vTemp.z * -s);
	z = (vTemp.y * +s) + (vTemp.z * +c);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::RotationY( Sint32 Angle )
{
	CVector3D vTemp = *this;
	Float s = Math::Sin( Angle );
	Float c = Math::Cos( Angle );

	x = (vTemp.x * +c) + (vTemp.z * +s);
	y = vTemp.y;
	z = (vTemp.x * -s) + (vTemp.z * +c);
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::RotationZ( Sint32 Angle )
{
	CVector3D vTemp = *this;
	Float s = Math::Sin( Angle );
	Float c = Math::Cos( Angle );

	x = (vTemp.x * +c) + (vTemp.y * -s);
	y = (vTemp.x * +s) + (vTemp.y * +c);
	z = vTemp.z;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::RotationAxis( Sint32 Angle, CVector3D &Axis )
{
	CMatrix Mat;
	Mat.RotationAxis( Angle, Axis );
	TransformNormal( Mat );
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Scaling( Float sx, Float sy, Float sz )
{
	x *= sx;
	y *= sy;
	z *= sz;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
void CVector3D::Translation( Float px, Float py, Float pz )
{
	x += px;
	y += py;
	z += pz;
}

