using namespace Selene;


//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CBaseMgr::CBaseMgr( CDevice *pDevice )
{
	m_pDevice = pDevice;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CBaseMgr::~CBaseMgr()
{
	CBaseObject *pObj = GetTop();
	while ( pObj != NULL )
	{
		CBaseObject *pNext = GetNext( pObj );
		delete pObj;
		pObj = pNext;
	}
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CDevice *CBaseMgr::GetDevicePointer( void ) const
{
	return m_pDevice;
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CGraphicCard *CBaseMgr::GetGraphicCardPointer( void ) const
{
	return GetDevicePointer()->GetGraphicCardPointer();
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CCore *CBaseMgr::GetCorePointer( void ) const
{
	return GetGraphicCardPointer()->GetCorePointer();
}

//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
HWND CBaseMgr::GetWindow( void ) const
{
	return m_pDevice->GetCreationWindow();
}
